# Выберите ваш любимый язык программирования

print('Выберите язык программирования: ')
choice = int(input("1. Python', '2. Java', '3. C', '4. C++', '5. Php"))

if choice == 1: print('Вы выбрали язык Python')
elif choice == 2: print('Вы выбрали язык Java')
elif choice == 3: print('Вы выбрали язык C')
elif choice == 4: print('Вы выбрали язык C++')
elif choice == 5: print('Вы выбрали язык Php')
else: print('Вы выбрали неизвестный язык программирования')